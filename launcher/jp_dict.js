const FULL_DICT = Object.assign({}, PLAYERS_JP, FOOTBALL_TERMS, GENERAL_TERMS);

let _cachedRe = null;
function _buildRe() {
  if (_cachedRe) return _cachedRe;
  const keys = Object.keys(FULL_DICT).sort((a, b) => b.length - a.length);
  const escaped = keys.map(k => k.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'));
  _cachedRe = new RegExp('(' + escaped.join('|') + ')', 'g');
  return _cachedRe;
}

function _replaceFootballRecord(text) {
  return String(text).replace(/(\d+)\s*勝\s*(\d+)\s*分(?:\s*(\d+)\s*敗)?/g, (_, w, d, l) =>
    l != null ? `${w}しょう ${d}わけ ${l}はい` : `${w}しょう ${d}わけ`
  );
}

function applyJpDict(text) {
  if (!text) return text;
  const pre = _replaceFootballRecord(text);
  return pre.replace(_buildRe(), (m) => FULL_DICT[m] || m);
}

module.exports = { applyJpDict, FULL_DICT };
